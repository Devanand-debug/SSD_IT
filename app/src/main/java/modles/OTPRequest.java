package modles;

public class OTPRequest {

    private String otpkey;
    private String otp;

    public String getOtpkey() {
        return otpkey;
    }

    public void setOtpkey(String otpkey) {
        this.otpkey = otpkey;
    }

    public String getOtp() {
        return otp;
    }

    public void setOtp(String otp) {
        this.otp = otp;
    }
}
